    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        tp=(int) (model.getValueAt(jTable1.getSelectedRow(),4));
        
        total_price=total_price-tp;
        model.setValueAt(jComboBox1.getSelectedItem(),jTable1.getSelectedRow(),1);
         model.setValueAt(jTextField1.getText(),jTable1.getSelectedRow(),2);
         model.setValueAt(jTextField2.getText(),jTable1.getSelectedRow(),3);
        
        
         num1=Integer.parseInt(jTextField1.getText());
       num2=Integer.parseInt(jTextField2.getText());
      
       price=num1*num2;
        model.setValueAt(price,jTable1.getSelectedRow(), 4);
         total_price=total_price+price;
         jLabel11.setText(""+total_price);
    }//GEN-LAST:event_jButton3ActionPerformed

